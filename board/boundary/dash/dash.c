int board_late_init(void)
{
	int cpurev = get_cpu_rev();

	setenv("cpu", get_imx_type((cpurev & 0xFF000) >> 12));
	if (!getenv("board"))
		setenv("board", "dash");
	setenv("uboot_defconfig", CONFIG_DEFCONFIG);
	return 0;
}
