void board_poweroff(void)
{
	struct snvs_regs *snvs = (struct snvs_regs *)(SNVS_BASE_ADDR);

	gpio_set_value(GP_MAIN_ON_OFF, 0);
	writel(0x60, &snvs->lpcr);
	mdelay(500);
}

